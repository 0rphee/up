#include <iostream>

// EJEMPLO TIPO DE DATOS
int main(){
    int i {324};
    printf("Es Int: %d\n", i );

    float f {324.4532};
    printf("Es Float: %f\n", f);

    long l {3425342544231};
    printf("Es long: %ld\n", l);

    char c {'A'};
    printf("Es long: %c\n", c);

    bool b {true};
    printf("Es bool: %d\n", b);

}





